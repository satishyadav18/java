package myjava;

class Cast {
   public static void main(Str[] args){
    float f = 5.6f;
    int a = (int)f;
    System.out.println(a);
    int b = 257;
    byte k = (byte)b;
    System.out.println(k);

    byte p = 10;
    byte q = 35;
    int result = p * q;
    System.out.println(result);

   } 
}
